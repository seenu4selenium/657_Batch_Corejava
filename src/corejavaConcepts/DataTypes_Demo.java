package corejavaConcepts;

public class DataTypes_Demo {

	public static void main(String[] args) {
		// User will store numbers into a variable.
		byte b = 127;
		short c = 345;
		int a = 968979;
		long d = 1874632210;

		// User will store number with decimal values into a variable.
		float f = 56.123456789f;
		double g = 56.123456789;
		System.out.println(f);
		System.out.println(g);

		// Want to store TRUE or FALSE, will use boolean
		boolean h = true;
		boolean i = false;

		// User will store number/Alphabet/SpecialCharacter into a variable.
		// char data type you can declare into single quotes
		char abc = '4';
		// char abc ='p';
		// char abc ='&';

	}

}
